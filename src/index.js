"use strict";
// Função de multiplicação
function multiplicacao(base, altura) {
    return base * altura;
}
// Função de saudação
function saudacao(nome) {
    return "Olá " + nome;
}
// Exemplos de uso das funções
console.log(multiplicacao(4, 6)); // Saída: 6
console.log(saudacao("João")); // Saída: Olá João
